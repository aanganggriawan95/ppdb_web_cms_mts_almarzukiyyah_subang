<script>
function previewFile(input, name) {
    if (input.files && input.files[0]) {
        const file = input.files[0];

        // tampilkan nama file
        document.getElementById('text-' + name).innerText = file.name;

        // tampilkan status ✔
        document.getElementById('status-' + name).classList.remove('hidden');

        // ubah warna box
        const box = document.getElementById('box-' + name);
        box.classList.remove('bg-gray-50');
        box.classList.add('bg-green-50', 'border-green-500');

        // ubah icon jadi centang
        const icon = document.getElementById('icon-' + name);
        icon.setAttribute('data-lucide', 'check-circle');

